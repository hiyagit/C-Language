#include<stdio.h>
void main()
{char i;
	
	printf("\nASCII value for upper case alphabets(A-Z) is:");
	for(i='A';i<='Z';i++)
	{
		printf(" %d",i);
	}
	printf("\nASCII value for lower case alphabets(a-z) is:");
	for(i='a';i<='z';i++)
	{
		printf(" %d",i);
	}
	printf("\nASCII value for digits(0-9) is:");
	for(i='0';i<='9';i++)
	{
		printf(" %d",i);
	}

}
